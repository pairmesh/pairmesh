package utils

import "math/rand"

func GenerateRandPayload(plen uint16) []byte {
	var alp = []byte("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ")
	b := make([]byte, plen)
	for i := range b {
		b[i] = alp[rand.Intn(len(alp))]
	}
	return b
}
